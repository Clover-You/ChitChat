public enum HttpError implements ErrorEnum {
    SUCCESS("200", "successful")
    ;

    private String code;

    private String message;

    HttpError(String code, String message) {
        this.code = code;
        this.message = message;
    }

    @Override
    public String getErrorCode() {
        return this.code;
    }

    @Override
    public String getErrorMessage() {
        return this.message;
    }
}
