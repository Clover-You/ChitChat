@Component
public class NettyServerHandlerInitializer extends ChannelInitializer<Channel> {

    private final WebSocketServerHandler webSocketServerHandler;

    @Autowired
    public NettyServerHandlerInitializer(WebSocketServerHandler webSocketServerHandler) {
        this.webSocketServerHandler = webSocketServerHandler;
    }

    /**
     * This method will be called once the {@link Channel} was registered. After the method returns this instance
     * will be removed from the {@link ChannelPipeline} of the {@link Channel}.
     *
     * @param channel the {@link Channel} which was registered.
     */
    @Override
    protected void initChannel(Channel channel) {
        var pipeline = channel.pipeline();
        pipeline.addLast(new HttpServerCodec());
        pipeline.addLast(new ChunkedWriteHandler());
        pipeline.addLast(new HttpObjectAggregator(65536));
        pipeline.addLast(new ChannelInboundHandlerAdapter() {
            @Override
            public void channelRead(ChannelHandlerContext ctx, Object msg) throws Exception {
                super.channelRead(ctx, msg);
            }
        });
        pipeline.addLast(new WebSocketServerCompressionHandler());
        pipeline.addLast(new WebSocketServerProtocolHandler("/socket", null, true, 10240));
        // 接收消息
        pipeline.addLast(this.webSocketServerHandler);
    }
}
